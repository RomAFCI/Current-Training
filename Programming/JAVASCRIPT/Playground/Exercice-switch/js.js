let jour = 1;

switch (jour) {
  case 1:
    console.log("Lundi, Spaghetti Bolognaise")
    break;

    case 2:
    console.log("Mardi, Poulet Rôti")
    break;

    case 3:
    console.log("Mercredi, Poisson pané")
    break;

    case 4:
    console.log("Jeudi, Salade composée")
    break;

    case 5:
    console.log("Vendredi, Pizza Margherita")
    break;

  default:
    console.log("Le restaurant n'est pas ouvert le week end")
    break;
}